// Examples
let score = 102;
let bonus = 25;
let totalScore = score + bonus
console.log(totalScore); // 127

// (1) addition
let addition = 4 + 5
console.log(addition);   // 9

// (2) subtraction
let subtract = 3 + 9
console.log(subtract);   // 12

// (3) mutliplicaion
let multiplication = 3 * 4
console.log(multiplication);  // 12

// (4) remainder
let division = 8 / 2
console.log(division)   // 4

// (5) remainder
let remainder = 9 % 2
console.log('remainder =', remainder);  // 1

// (6) Exonential
let expo = 2 ** 3
console.log('expo =', expo);  // 8


// Increament
let myScore = 110;
myScore++
console.log(myScore);   // 111

// Decreament
let credits = 56;
credits--
console.log(credits);  // 57


// Comparison operator
let num1 = 10;
let num2 = '10';
let num3 = 20;

console.log(num1 == num2);  // true
console.log(num1 != num3);  // true
